/**
 * VoiceButton — mic toggle for orchestrator voice mode.
 *
 * States:
 *   off        → show mic icon (click to start)
 *   connecting → show spinner
 *   active     → show animated mic (listening via server VAD)
 *   speaking   → show speaker wave (model is speaking)
 *   thinking   → show thinking indicator
 *   tool_use   → show gear icon (tool executing)
 *   error      → show error icon (click to retry)
 */

import type { VoiceStatus } from "../types";

interface Props {
  status: VoiceStatus;
  onStart: () => void;
  onStop: () => void;
}

export function VoiceButton({ status, onStart, onStop }: Props) {
  const isOff = status === "off" || status === "error";
  const label = {
    off: "Start voice",
    connecting: "Connecting…",
    active: "Voice active — stop",
    speaking: "Assistant speaking — stop",
    thinking: "Thinking… — stop",
    tool_use: "Using tool… — stop",
    error: "Voice error — retry",
  }[status];

  return (
    <button
      className={`voice-btn voice-btn--${status}`}
      onClick={isOff ? onStart : onStop}
      title={label}
      aria-label={label}
    >
      {status === "off" && <MicIcon />}
      {status === "connecting" && <SpinnerIcon />}
      {status === "active" && <MicActiveIcon />}
      {status === "speaking" && <SpeakerIcon />}
      {status === "thinking" && <ThinkingIcon />}
      {status === "tool_use" && <GearIcon />}
      {status === "error" && <ErrorIcon />}
    </button>
  );
}

